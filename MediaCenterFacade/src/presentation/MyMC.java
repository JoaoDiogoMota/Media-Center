    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        JTable place = (JTable)evt.getSource();
        int row = place.rowAtPoint(evt.getPoint());
        String key=place.getModel().getValueAt(row, 0)+"";

        try{
            if(runner != null && runner.isAlive()) runner.destroy();
            runner = mediacenter.playConteudo(mediacenter.getConteudo(key));
        }
        catch(IOException e) {
            e.getMessage();
        }
    }//GEN-LAST:event_jTable1MouseClicked

