    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        String selectedValue = jComboBox1.getSelectedItem().toString();
        if (selectedValue.equals("Colecao")){
            List<Conteudo> contC = mediacenter.getConteudos(utilizador.getEmail());
            Object[][] data = new Object[contC.size()][3];
            for(int i = 0; i<contC.size(); i++){
                Conteudo aux = contC.get(i);
                data[i][0] = aux.getIdConteudo();
                data[i][1] = aux.getDuracao();
                data[i][2] = aux.getCategoria();
            }
            DefaultTableModel model = new DefaultTableModel(data,new String[] {"Nome", "Duração", "Categoria"});
            jTable1.setModel(model);
        }
        else if (selectedValue.equals("Biblioteca")){
            List<Conteudo> contB = mediacenter.getBiblioteca();
            Object[][] data = new Object[contB.size()][3];
            for(int i = 0; i<contB.size(); i++){
                Conteudo aux = contB.get(i);
                data[i][0] = aux.getIdConteudo();
                data[i][1] = aux.getDuracao();
                data[i][2] = aux.getCategoria();
            }
            DefaultTableModel model = new DefaultTableModel(data,new String[] {"Nome", "Duração", "Categoria"});
            jTable1.setModel(model);
        }
        else{
            List<Conteudo> contL = utilizador.getListas().getContLista(selectedValue,utilizador.getEmail());
            Object[][] data = new Object[contL.size()][3];
            for(int i = 0; i<contL.size(); i++){
                Conteudo aux = contL.get(i);
                data[i][0] = aux.getIdConteudo();
                data[i][1] = aux.getDuracao();
                data[i][2] = aux.getCategoria();
            }
            DefaultTableModel model = new DefaultTableModel(data,new String[] {"Nome", "Duração", "Categoria"});
            jTable1.setModel(model);
        }
    
    }//GEN-LAST:event_jComboBox1ActionPerformed

